#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int a;
    // Do not print anything before taking input
    cin >> a;            // Read integer input
    
    // Output the value of a in the line below
    cout << a;           // Print the same value
}

/*
Time Complexity:
- O(1), constant time for input and output.

Space Complexity:
- O(1), constant extra space.
*/